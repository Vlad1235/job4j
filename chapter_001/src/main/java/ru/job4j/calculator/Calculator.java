package ru.job4j.calculator;

/**
 *  * Calculator.
 *  * @author Vladislav (fn1235@bk.ru)
 */

public class Calculator {
    /**
     * Method add.
     * @param first.
     * @param second.
     * @return first + second.
     */

    public double add(double first, double second) {
        return first + second;
    }
    /**
     * Method subtract.
     * @param first.
     * @param second.
     * @return first - second.
     */
    public double subtract(double first, double second) {
        return first - second;
    }

    /**
     * Method div.
     * @param first.
     * @param second.
     * @return first/second.
     */
    public double div(double first, double second) {
        return first/second;
    }

    /**
     * Method multiple.
     * @param first.
     * @param second.
     * @return first*second.
     */
    public double multiple(double first, double second) {
        return first*second;
    }

}
